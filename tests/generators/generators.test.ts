describe('getGenerators', () => {
  it('should return all 6 generators', () => {
    const all = getGenerators(['claude', 'cursor', 'copilot', 'windsurf', 'aider', 'codex'])
    expect(all).toHaveLength(6)
  })

  it('should return subset of generators', () => {
    const subset = getGenerators(['claude', 'cursor'])
    expect(subset).toHaveLength(2)
  })
})

describe('empty instructions', () => {
  it('should not error with empty instructions', () => {
    const emptyConfig: AiCodingRulesConfig = {
      version: '1',
      project: {
        name: 'empty',
        type: 'library',
        language: 'typescript',
      },
      rules: {},
      structure: {},
      instructions: {},
      targets: ['claude'],
    }
    const gen = getGenerator('claude')
    expect(() => gen.generate(emptyConfig, mockScan)).not.toThrow()
  })
})
