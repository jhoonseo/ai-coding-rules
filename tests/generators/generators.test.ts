describe('AiderGenerator', () => {
  it('should generate valid YAML', () => {
    const gen = getGenerator('aider') as AiderGenerator
    const output = gen.generate(mockConfig, mockScan)
    expect(output).toContain('lint-cmd:')
    expect(output).toContain('test-cmd:')
    expect(output).toContain('auto-commits:')
    expect(output).toContain('CONVENTIONS.md')
  })

  it('should generate conventions content', () => {
    const gen = new AiderGenerator()
    gen.generate(mockConfig, mockScan)
    const conventions = gen.getConventionsContent()
    expect(conventions).toContain('test-project')
    expect(conventions).toContain('Generated by rulegen')
  })

  it('should output to .aider.conf.yml', () => {
    expect(getGenerator('aider').outputPath).toBe('.aider.conf.yml')
  })
})

