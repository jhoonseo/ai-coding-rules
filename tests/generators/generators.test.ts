describe('ClaudeGenerator', () => {
  const gen = getGenerator('claude')

  it('should generate CLAUDE.md with watermark', () => {
    const output = gen.generate(mockConfig, mockScan)
    expect(output).toContain('Generated by rulegen')
  })

  it('should have build commands at the top', () => {
    const output = gen.generate(mockConfig, mockScan)
    const overviewIdx = output.indexOf('# test-project')
    const buildIdx = output.indexOf('## Build & Test Commands')
    const techIdx = output.indexOf('## Tech Stack')
    expect(buildIdx).toBeGreaterThan(overviewIdx)
    expect(buildIdx).toBeLessThan(techIdx)
  })

  it('should include pnpm commands', () => {
    const output = gen.generate(mockConfig, mockScan)
    expect(output).toContain('pnpm run build')
    expect(output).toContain('pnpm run test')
  })

  it('should include overrides for claude', () => {
    const output = gen.generate(mockConfig, mockScan)
    expect(output).toContain('Always run tests before committing')
  })

  it('should output to CLAUDE.md', () => {
    expect(gen.outputPath).toBe('CLAUDE.md')
  })
})

